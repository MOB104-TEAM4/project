    public ArrayList<taikhoan> list1() {
        ArrayList<taikhoan> ds = new ArrayList<>();

        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, user, pass);
            String sql = "select * from HinhAnh";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            taikhoan tk;
            while (rs.next()) {
                tk = new taikhoan(rs.getString("MaUser"), rs.getBytes("images"));
                ds.add(tk);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Lỗi bảng");
        }
        return ds;
    }

    public void database() {
        ArrayList<taikhoan> ds = list1();
        DefaultTableModel dtm = (DefaultTableModel) tbl_tk.getModel();
        Object[] row = new Object[2];
        for (int i = 0; i < ds.size(); i++) {
            row[0] = ds.get(i).getUser();
            row[1] = ds.get(i).getAnh();
            dtm.addRow(row);
        }
    }

