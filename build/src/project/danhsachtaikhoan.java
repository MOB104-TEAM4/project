    private void lblhinhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblhinhMouseClicked
        // TODO add your handling code here:
        JFileChooser f = new JFileChooser();
        f.setDialogTitle("Open Image");
        f.showOpenDialog(null);
        File tenanh = f.getSelectedFile();
        duongdananh = tenanh.getAbsolutePath();
        ImageIcon imageIcon = new ImageIcon(new ImageIcon(duongdananh).getImage().getScaledInstance(lblhinh.getWidth(), lblhinh.getHeight(), Image.SCALE_SMOOTH));
        lblhinh.setIcon(imageIcon);
        try{
            File hinh = new File(duongdananh);
            FileInputStream fis = new FileInputStream(hinh);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            for(int num;(num=fis.read(buf)) != -1;){
                bos.write(buf,0,num);
            }
            anh=bos.toByteArray();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Lỗi bảng");
        }
    }//GEN-LAST:event_lblhinhMouseClicked

